function initHero(){
  const videos=Array.from(document.querySelectorAll('.hero-video'));
  const bar=document.getElementById('heroBar');
  if(!videos.length) return;

  function preloadNext(){
    const next=(currentHeroIdx+1)%videos.length;
    videos[next].preload='auto';
    try{ videos[next].load(); }catch(e){}
  }

  function showVideo(idx){
    videos.forEach((v,i)=>{
      v.classList.toggle('active',i===idx);
      if(i===idx){ try{v.play();}catch(e){} }
    });
    if(bar){ bar.style.width='0'; setTimeout(()=>bar.style.width='100%',60); }
  }

  function nextVideo(){
    currentHeroIdx=(currentHeroIdx+1)%videos.length;
    showVideo(currentHeroIdx);
    preloadNext();
  }

  showVideo(0);
  preloadNext();
  if(bar){ bar.style.transition='width 6s linear'; setTimeout(()=>bar.style.width='100%',120); }
  if(heroTimer) clearInterval(heroTimer);
  heroTimer=setInterval(nextVideo,6000);
}

